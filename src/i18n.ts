const dicts: Record<Language, Dict> = { en, it }

export function translate(lang: Language | null, key: string): string {
  const l = lang ?? 'en'
  return dicts[l][key] ?? en[key] ?? key
}

/** Hook returning the translate function bound to the current language. */
export function useT(): (key: string) => string {
  const { language } = useSettings()
  return (key: string) => translate(language, key)
}
